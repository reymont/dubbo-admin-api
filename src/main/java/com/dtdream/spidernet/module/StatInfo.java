package com.dtdream.spidernet.module;

public class StatInfo {
	private int ServiceTotalNum;
	private int AppTotalNum;
	private int ProviderTotalNum;
	private int ConsumerTotalNum;
	
	public int getServiceTotalNum() {
		return ServiceTotalNum;
	}
	public void setServiceTotalNum(int serviceTotalNum) {
		ServiceTotalNum = serviceTotalNum;
	}
	public int getAppTotalNum() {
		return AppTotalNum;
	}
	public void setAppTotalNum(int appTotalNum) {
		AppTotalNum = appTotalNum;
	}
	public int getProviderTotalNum() {
		return ProviderTotalNum;
	}
	public void setProviderTotalNum(int providerTotalNum) {
		ProviderTotalNum = providerTotalNum;
	}
	public int getConsumerTotalNum() {
		return ConsumerTotalNum;
	}
	public void setConsumerTotalNum(int consumerTotalNum) {
		ConsumerTotalNum = consumerTotalNum;
	}
}
